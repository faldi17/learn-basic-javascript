// greater than
let result = 5 > 10
document.writeln("<p>" + result + "</p>")

// less than
result = 5 < 10
document.writeln("<p>" + result + "</p>")

// greater than or equal to
result = 5 >= 10
document.writeln("<p>" + result + "</p>")

// less than or equal to
result = 5 <= 10
document.writeln("<p>" + result + "</p>")

// equality operator
result = 5 == "5"
document.writeln("<p>" + result + "</p>")

// strict equality operator
result = 5 === "5";
document.writeln("<p>" + result + "</p>")

// inequality operator
result = 5 != "5"
document.writeln("<p>" + result + "</p>")

// strict inequality operator
result = 5 !== "5"
document.writeln("<p>" + result + "</p>")
