// positive
let result = +1
document.writeln("<p>" + result + "</p>")

// increment
result++ // result = result + 1 or result += 1
document.writeln("<p>" + result + "</p>")

// decrement
result-- // result = result - 1 or result -= 1
document.writeln("<p>" + result + "</p>")

// negative
result = -result // -1
document.writeln("<p>" + result + "</p>")